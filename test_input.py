fretboard = presets["acoustic_guitar"]

parsed_blocks = parse_tab_text(text, fretboard)

print("blocks:", len(parsed_blocks))
for i, block in enumerate(parsed_blocks):
    print(
        f"block {i}: measures={block.parsed_tab.measure_count}, "
        f"repeat={block.raw_block.repeat}, "
        f"events={len(block.parsed_tab.events)}"
    )

events = to_realized_events(parsed_blocks)
print("flattened events:", len(events))
renderer = TabRenderer(fretboard)
print(renderer.render(events, measures_per_line=2))
